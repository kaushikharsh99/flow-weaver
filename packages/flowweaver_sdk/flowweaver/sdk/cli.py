import os
import sys
import argparse

def create_plugin(name: str):
    """Generates a new plugin folder structure based on the platform template."""
    safe_name = name.lower().replace("-", "_").replace(" ", "_")
    base_path = os.path.join(os.getcwd(), name)
    
    if os.path.exists(base_path):
        print(f"Error: Directory '{name}' already exists.")
        sys.exit(1)
        
    print(f"Creating plugin template at: {base_path}...")
    
    # Create directories
    pkg_path = os.path.join(base_path, safe_name)
    os.makedirs(pkg_path, exist_ok=True)
    
    # Write plugin.yaml configuration
    plugin_yaml = f"""id: {safe_name}
name: {name.replace('_', ' ').replace('-', ' ').title()}
version: 0.1.0
author: Developer
description: A custom data processing plugin for FlowWeaver.
nodes:
  - {safe_name}.nodes.CustomProcessingNode
"""
    with open(os.path.join(base_path, "plugin.yaml"), "w") as f:
        f.write(plugin_yaml.strip() + "\n")
        
    # Write requirements.txt
    with open(os.path.join(base_path, "requirements.txt"), "w") as f:
        f.write("# Add additional plugin pip requirements here\n")
        
    # Write package __init__.py
    with open(os.path.join(pkg_path, "__init__.py"), "w") as f:
        f.write(f"# {name} package\n")
        
    # Write package nodes.py with a sample Node implementation
    nodes_py = f"""from typing import Dict, Any
from flowweaver.sdk import Node, Port, Parameter, ExecutionContext, TabularDataset

class CustomProcessingNode(Node):
    id = "{safe_name}_processor"
    label = "Custom Processor"
    category = "Transform"
    description = "A custom node template generated by FlowWeaver CLI."
    icon = "Wand2"
    color = "#7a4fc6"
    inputs = [Port(id="in", label="rows", type="tabular", required=True)]
    outputs = [Port(id="out", label="rows", type="tabular")]
    params_schema = [
        Parameter(key="suffix", label="Text suffix", type="text", default="_processed"),
    ]

    def execute(self, inputs: Dict[str, Any], ctx: ExecutionContext) -> Dict[str, Any]:
        dataset = inputs.get("in")
        if not dataset:
            return {{"out": TabularDataset([], columns=[])}}
            
        columns = dataset.columns()
        rows = dataset.to_list()
        suffix = ctx.parameters.get("suffix", "_processed")
        
        ctx.log(f"Appending suffix '{{suffix}}' to all cell values.")
        
        processed_rows = []
        for row in rows:
            new_row = {{}}
            for k, v in row.items():
                new_row[k] = f"{{v}}{{suffix}}"
            processed_rows.append(new_row)
            
        return {{"out": TabularDataset(processed_rows, columns=columns)}}
"""
    with open(os.path.join(pkg_path, "nodes.py"), "w") as f:
        f.write(nodes_py.strip() + "\n")
        
    # Write README.md
    readme = f"""# {name}

A custom FlowWeaver plugin.

## Installation

Register this plugin directory inside the FlowWeaver plugins directory, or deploy it.
"""
    with open(os.path.join(base_path, "README.md"), "w") as f:
        f.write(readme.strip() + "\n")
        
    print(f"Successfully generated plugin template '{name}'!")
    print("Folder contents:")
    print(f"  - {name}/plugin.yaml")
    print(f"  - {name}/requirements.txt")
    print(f"  - {name}/{safe_name}/nodes.py")

def main():
    parser = argparse.ArgumentParser(description="FlowWeaver CLI developer assistant tool.")
    subparsers = parser.add_subparsers(dest="command", required=True)
    
    # create-plugin subcommand
    create_parser = subparsers.add_parser("create-plugin", help="Scaffold a new custom plugin template.")
    create_parser.add_argument("name", type=str, help="Name of the plugin directory to create.")
    
    args = parser.parse_args()
    
    if args.command == "create-plugin":
        create_plugin(args.name)

if __name__ == "__main__":
    main()
